def enrich_cpv(code: str) -> dict:
    """Gibt Label, Kurzkategorie und Elterncode für einen CPV-Code zurück."""
    c = str(code).strip()
    label    = CPV_LABELS.get(c, "")
    division = c[:2]
    category_label = CPV_DIVISIONS.get(division, f"Division {division}")

    # Elterncode: auf Group-Ebene (erste 3 Ziffern + 5x0)
    parent = ""
    if len(c) >= 8:
        parent = c[:3] + "00000"
        if parent == c:
            parent = c[:2] + "000000"

    return {
        "code":           c,
        "label":          label or f"CPV {c}",
        "division":       division,
        "category":       category_label,
        "parent":         parent,
    }

def enrich_cpv_list(codes: list) -> list[dict]:
    return [enrich_cpv(c) for c in codes if c]


