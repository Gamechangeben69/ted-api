def enrich_nuts(code: str) -> dict:
    """Gibt Ländername und Regionsname für einen NUTS-Code zurück."""
    c = str(code).strip().upper()
    country_iso = c[:2]
    country     = NUTS_COUNTRIES.get(country_iso, country_iso)
    region      = NUTS_REGIONS.get(c, "") or NUTS_REGIONS.get(c[:3], "") or ""
    return {
        "nuts_code":    c,
        "country_code": country_iso,
        "country":      country,
        "region":       region,
    }
